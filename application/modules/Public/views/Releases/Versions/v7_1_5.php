        With our MyMI Wallets Feature, you can also integrate External Financial Accounts to your Investor Dashboard to track your Financial Growth across all of your platforms.<br>
        Currently, in this Beta Release, we only have established connectivity with TD Ameritrade, but we expect to continuously release new integrations over the next few months as we develop/deploy them into our Beta/Production Environments. 
        <br>
        <br>
        You have (2) separate methods of adding/integrating Additional Wallets in your MyMI Account. You can manually add any account that we do not currently provide a Premium Brokerage Integration Feature for. 
        For those Manually Added Accounts, you will have the ability to export your financial data and import any transactions associated with that account into our database to further continue analyzing your financial portfolio. 
        This will forever be available for all members who choose to utilize our platform FREE of cost! 
        <br>
        <br>
        Our secondary and most advanced option is utilizing our Premium Brokerage Integration to automatically import your financial account and transactions appended to that data. 
        For security purposes, you will be the sole reader/writer of that information and you can add/edit/update/delete as you wish and please. 
        Any financial data that MyMI Wallet utilizes will not have any of your personal information included or shared within our platform or any 3rd-party entity. 
        This financial data will only include transactions conducted, but will not append any account or personal information as it is saved in a separate database.
